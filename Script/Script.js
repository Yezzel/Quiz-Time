const questionElement = document.getElementById('question');
const answerButtonElement = document.getElementById('answer-button');
const nextButtonElement = document.getElementById('next-btn');

let score = 0;
let currentQuestionIndex = 0;

function startQuiz() {
  currentQuestionIndex = 0;
  score = 0;
  nextButtonElement.innerHTML = 'Next';
  showQuestion();
}
function showQuestion() {
  resetQuiz();
  let currentQuestion = questions[currentQuestionIndex]
  let questionNo = currentQuestionIndex + 1;
  questionElement.innerHTML = questionNo + ". " + currentQuestion.Question;

  currentQuestion.Answers.forEach(answer => {
    const button = document.createElement('button');
    button.innerHTML = answer.text;
    button.classList.add('btn');
    answerButtonElement.appendChild(button);
    if(answer.correct) {
      button.dataset.correct = answer.correct
    }
    button.addEventListener('click', selectAnswer)
  })

}
function selectAnswer(e) {
  const selectAnswer = e.target;
  const correctAnswer = selectAnswer.dataset.correct === 'true';
  if(correctAnswer) {
    selectAnswer.classList.add('correct');
    score++;
  }else{
    selectAnswer.classList.add('incorrect');
  }
  Array.from(answerButtonElement.children).forEach(button => {
    if(button.dataset.correct === 'true') {
      button.classList.add('correct');
    }
    button.disable = 'true';
    });
      nextButtonElement.style.display = 'block';
}

function resetQuiz() {
  nextButtonElement.style.display = 'none';
  while(answerButtonElement.firstChild) {
    answerButtonElement.firstChild.remove();
  }
}
function showScore() {
  resetQuiz();
  questionElement.innerHTML = `You scored ${score} out of ${questions.length}`;
  nextButtonElement.innerHTML = 'Play Again';
  nextButtonElement.style.display = 'block'
}
function handleNextBtn() {
  currentQuestionIndex ++;
  if(currentQuestionIndex < questions.length) {
    showQuestion();
  }else{
    showScore();
  }
}
startQuiz();

nextButtonElement.addEventListener('click', () => {
  if(currentQuestionIndex < questions.length) {
    handleNextBtn();
  }else{
    startQuiz();
  }
})